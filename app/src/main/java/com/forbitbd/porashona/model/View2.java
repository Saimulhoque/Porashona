package com.forbitbd.porashona.model;

public class View2 {

    String letter;
    String title;

    public View2(String letter, String title) {
        this.letter = letter;
        this.title = title;
    }

    public String getLetter() {
        return letter;
    }

    public void setLetter(String letter) {
        this.letter = letter;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
